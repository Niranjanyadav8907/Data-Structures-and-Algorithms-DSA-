class Swap
{
	public static void main(String[] args)
	{
		int a=18;
		int b=20;
		System.out.println("a before swapping is: "+a);
		System.out.println("b before swapping is: "+b);
		int c=b;
		b=a;
		a=c;
		System.out.println("========================");
		System.out.println("a after swapping is: "+a);
		System.out.println("b after swapping is: "+b);
	}
}



